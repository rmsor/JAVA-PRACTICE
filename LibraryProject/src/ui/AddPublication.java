    private void addPublicationBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addPublicationBtnActionPerformed
        // TODO add your handling code here:

        String typeOfPublication = typeComboBox.getSelectedItem().toString();
        String title = titleTF.getText();

        if (typeOfPublication.equalsIgnoreCase("Book")) {
            String isbn = isbnTF.getText();
//            String firstName = firstNameTF.getText();
//            String lastName = lastNameTF.getText();
//            String phone = phoneTF.getText();
//            String credentials = credentialsTF.getText();
//            String shortBio = shortBioTF.getText();
//            String street = streetTF.getText();
//            String city = cityTF.getText();
//            String state = stateTF.getText();
//            String zip = zipTF.getText();

            List<Author> authorList = new ArrayList<Author>();

            DefaultTableModel model = (DefaultTableModel) authorAddressTable.getModel();

            if (model.getRowCount() > 0) {
                for (int i = model.getRowCount() - 1; i > -1; i--) {
                    String firstName = authorAddressTable.getValueAt(i, 0).toString();
                    String lastName = authorAddressTable.getValueAt(i, 1).toString();
                    String phone = authorAddressTable.getValueAt(i, 2).toString();
                    String credentials = authorAddressTable.getValueAt(i, 3).toString();
                    String shortBio = authorAddressTable.getValueAt(i, 4).toString();
                    String street = authorAddressTable.getValueAt(i, 5).toString();
                    String city = authorAddressTable.getValueAt(i, 6).toString();
                    String state = authorAddressTable.getValueAt(i, 7).toString();
                    String zip = authorAddressTable.getValueAt(i, 8).toString();

                    Author author = new Author(firstName, lastName, phone, credentials,shortBio, street, city, state, zip);

                    authorList.add(author);

                }
            }

            Collections.reverse(authorList);

            Book book = new Book(title, isbn, authorList);
            LibrarySystemApplication.getInstance().getPubBookList().add(book);
            DefaultTableModel modelBook = (DefaultTableModel) bookListTable.getModel();
            modelBook.addRow(new Object[]{title, isbn, showAuthors(authorList)});
            JOptionPane.showMessageDialog(null, "Successfully Book Publication Added");

        } else if (typeOfPublication.equalsIgnoreCase("Magazine")) {

            String issueNo = issueNoTF.getText();
            Magazine magazine = new Magazine(title, issueNo);
            LibrarySystemApplication.getInstance().getPubMagazineList().add(magazine);
            DefaultTableModel modelMag = (DefaultTableModel) magazineListTable.getModel();
            modelMag.addRow(new Object[]{title});
            JOptionPane.showMessageDialog(null, "Successfully Magazine Publication Added");

        }

        DefaultTableModel model = (DefaultTableModel) authorAddressTable.getModel();

        if (model.getRowCount() > 0) {
            for (int i = model.getRowCount() - 1; i > -1; i--) {
                model.removeRow(i);
            }
        }
        titleTF.setText("");
        issueNoTF.setText("");
        isbnTF.setText("");

        titleTF.requestFocus();


    }//GEN-LAST:event_addPublicationBtnActionPerformed
    public static String showAuthors(List<Author> authors){
        String names="";
        for(Author auth:authors){
            names+=auth.getFullName()+", ";
        }
        return names;
    }
