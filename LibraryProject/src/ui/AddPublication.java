    private void addAuthorAddressBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addAuthorAddressBtnActionPerformed
        // TODO add your handling code here:

        String firstName = firstNameTF.getText();
        String lastName = lastNameTF.getText();
        String phone = phoneTF.getText();
        String credentials = credentialsTF.getText();
        String shortBio = shortBioTF.getText();
        String street = streetTF.getText();
        String city = cityTF.getText();
        String state = stateTF.getText();
        String zip = zipTF.getText();

        if (firstName.length() == 0 || lastName.length() == 0
                || phone.length() == 0 || credentials.length() == 0 || shortBio.length() == 0
                || street.length() == 0 || city.length() == 0 || state.length() == 0
                || zip.length() == 0) {
            JOptionPane.showMessageDialog(null, "Please Fillup all fields of Author and Address");

        } else {
            DefaultTableModel model = (DefaultTableModel) authorAddressTable.getModel();
            model.addRow(new Object[]{firstName, lastName, phone, credentials, shortBio, street, city, state, zip});

            clearAuthorAddress();
            firstNameTF.requestFocus();

        }

    }//GEN-LAST:event_addAuthorAddressBtnActionPerformed

